class Blackbird_ContentManager_Block_Adminhtml_ContentType_Import_Tab_Form extends Mage_Adminhtml_Block_Widget_Form
{
    protected function _prepareForm()
    {
        $form = new Varien_Data_Form();
        $this->setForm($form);
        $fieldset = $form->addFieldset('contenttype_form', array('legend'=>Mage::helper('contentmanager')->__('Import content type')));
       
        $fieldset->addField('file', 'file', array(
            'label'     => 'Import file',
            'note'      => 'You can import JSON file. Previously exported from the content type edit page. Useful to import/export content type from local to remote server.',
            'name'      => 'file',
        ));

        return parent::_prepareForm();
    }
}